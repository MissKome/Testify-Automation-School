package Task;

public class Task4 {
    public static void main(String[] args) {
        //Area of a circle
        double radius = 12.7; //radius in meters
        double pi = 3.14;  //π
        double area = pi * radius * radius; //area of a circle is A=πr2

        // Print the result
        System.out.println("The area of the circle with radius " + radius + "m is " + area + " meters.");
    }
}
